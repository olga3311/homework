package lesson03.classwwork;

public class main01 {
    public static void main(String[] args) {
        //объявляем целочисленную переменную и записываем в нее число 10
        int number1 = 10;
        //объявляем вещественную переменную и записываем в нее число 5,25
        //Академически, это число с плавающей точкой или floating point number
        double number2 = 5.25;
        // объявляем символьную пееменную и заисываем символ а
        char letter = 'a';

        System.out.println(number1);
        System.out.println(number2);
        System.out.println(letter);
    }
}
